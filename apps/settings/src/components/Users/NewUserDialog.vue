<template>
	<NcDialog
		class="dialog"
		size="small"
		:name="t('settings', 'New account')"
		out-transition
		v-on="$listeners">
		<form
			id="new-user-form"
			class="dialog__form"
			data-test="form"
			:disabled="loading.all"
			@submit.prevent="createUser">
			<UserFormFields
				ref="fields"
				:formData="newUser"
				:fieldConfig="fieldConfig"
				:quotaOptions="quotaOptions" />
		</form>

		<template #actions>
			<NcButton
				class="dialog__submit"
				data-test="submit"
				form="new-user-form"
				variant="primary"
				type="submit">
				{{ t('settings', 'Add new account') }}
			</NcButton>
		</template>
	</NcDialog>
</template>

<script>
import NcButton from '@nextcloud/vue/components/NcButton'
import NcDialog from '@nextcloud/vue/components/NcDialog'
import UserFormFields from './UserFormFields.vue'

export default {
	name: 'NewUserDialog',

	components: {
		NcButton,
		NcDialog,
		UserFormFields,
	},

	props: {
		loading: {
			type: Object,
			required: true,
		},

		newUser: {
			type: Object,
			required: true,
		},

		quotaOptions: {
			type: Array,
			required: true,
		},
	},

	emits: ['closing', 'reset'],

	computed: {
		settings() {
			return this.$store.getters.getServerData
		},

		usernameLabel() {
			if (this.settings.newUserGenerateUserID) {
				return t('settings', 'Account name will be autogenerated')
			}
			return t('settings', 'Account name (required)')
		},

		/**
		 * Reactive field configuration passed to UserFormFields.
		 * Controls visibility, labels, and required state for each field
		 * based on the current form values and server settings.
		 */
		fieldConfig() {
			return {
				username: {
					show: true,
					label: this.usernameLabel,
					disabled: this.settings.newUserGenerateUserID,
					required: true,
				},

				password: {
					label: this.newUser.email === ''
						? t('settings', 'Password (required)')
						: t('settings', 'Password'),
					required: this.newUser.email === '',
				},

				email: {
					label: this.newUser.password === '' || this.settings.newUserRequireEmail
						? t('settings', 'Email (required)')
						: t('settings', 'Email'),
					required: this.newUser.password === '' || this.settings.newUserRequireEmail,
				},

				showPasswordEmailHint: !this.settings.newUserRequireEmail,
			}
		},
	},

	mounted() {
		this.$refs.fields?.focusField('username')
	},

	methods: {
		async createUser() {
			this.loading.all = true
			try {
				await this.$store.dispatch('addUser', {
					userid: this.newUser.username,
					password: this.newUser.password,
					displayName: this.newUser.displayName,
					email: this.newUser.email,
					groups: this.newUser.groups.map(({ id }) => id),
					subadmin: this.newUser.subadminGroups.map(({ id }) => id),
					quota: this.newUser.quota.id,
					language: this.newUser.language.code,
					manager: this.newUser.manager.id,
				})

				this.$emit('reset')
				this.$refs.fields?.focusField('username')
				this.$emit('closing')
			} catch (error) {
				this.loading.all = false
				if (error.response?.data?.ocs?.meta) {
					const statuscode = error.response.data.ocs.meta.statuscode
					if (statuscode === 102) {
						this.$refs.fields?.focusField('username')
					} else if (statuscode === 107) {
						this.$refs.fields?.focusField('password')
					}
				}
			}
		},
	},
}
</script>

