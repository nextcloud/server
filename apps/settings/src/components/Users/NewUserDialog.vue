<template>
	<NcDialog
		class="dialog"
		size="small"
		:name="t('settings', 'New account')"
		outTransition
		:noClose="loading.all"
		@closing="$emit('closing')">
		<form
			id="new-user-form"
			class="dialog__form"
			data-test="form"
			:inert="loading.all"
			:aria-busy="loading.all ? 'true' : 'false'"
			@submit.prevent="createUser">
			<UserFormFields
				ref="fields"
				:fieldConfig="fieldConfig"
				:quotaOptions="quotaOptions" />
		</form>

		<template #actions>
			<NcButton
				class="dialog__submit"
				data-test="submit"
				form="new-user-form"
				variant="primary"
				type="submit"
				:aria-disabled="loading.all ? 'true' : 'false'">
				<template v-if="loading.all" #icon>
					<NcLoadingIcon />
				</template>
				{{ loading.all ? t('settings', 'Adding new account …') : t('settings', 'Add new account') }}
			</NcButton>
		</template>
	</NcDialog>
</template>

<script setup lang="ts">
import type { FormData, QuotaOption } from './userFormUtils.ts'

import { translate as t } from '@nextcloud/l10n'
import { computed, onMounted, provide, ref } from 'vue'
import NcButton from '@nextcloud/vue/components/NcButton'
import NcDialog from '@nextcloud/vue/components/NcDialog'
import NcLoadingIcon from '@nextcloud/vue/components/NcLoadingIcon'
import UserFormFields from './UserFormFields.vue'
import { useStore } from '../../store/index.js'
import { formDataKey } from './injectionKeys.ts'

const props = defineProps<{
	loading: { all: boolean }
	newUser: FormData
	quotaOptions: QuotaOption[]
}>()

const emit = defineEmits<{
	closing: []
}>()

const store = useStore()

const fields = ref<{ focusField: (name: 'username' | 'password') => void } | null>(null)

// Children inject and mutate this object's properties; never reassign it or the
// injected reference goes stale.
provide(formDataKey, props.newUser)

const settings = computed(() => store.getters.getServerData)

const usernameLabel = computed(() => settings.value.newUserGenerateUserID
	? t('settings', 'Account name will be autogenerated')
	: t('settings', 'Account name (required)'))

const fieldConfig = computed(() => ({
	username: {
		show: true,
		label: usernameLabel.value,
		disabled: settings.value.newUserGenerateUserID,
		required: true,
	},

	password: {
		label: props.newUser.email === ''
			? t('settings', 'Password (required)')
			: t('settings', 'Password'),

		required: props.newUser.email === '',
	},

	email: {
		label: props.newUser.password === '' || settings.value.newUserRequireEmail
			? t('settings', 'Email (required)')
			: t('settings', 'Email'),

		required: props.newUser.password === '' || settings.value.newUserRequireEmail,
	},

	showPasswordEmailHint: !settings.value.newUserRequireEmail,
}))

onMounted(() => fields.value?.focusField('username'))

/**
 * Create the account from the current form state.
 */
async function createUser() {
	// Guard against re-submit while a request is already running. The
	// button is only aria-disabled (not disabled), so it can still fire.
	if (props.loading.all) {
		return
	}
	props.loading.all = true
	try {
		await store.dispatch('addUser', {
			userid: props.newUser.username,
			password: props.newUser.password,
			displayName: props.newUser.displayName,
			email: props.newUser.email,
			groups: props.newUser.groups.map(({ id }) => id),
			subadmin: props.newUser.subadminGroups.map(({ id }) => id),
			quota: props.newUser.quota.id,
			language: props.newUser.language.code,
			manager: (props.newUser.manager as { id?: string }).id,
		})

		emit('closing')
	} catch (error) {
		props.loading.all = false
		const meta = (error as { response?: { data?: { ocs?: { meta?: { statuscode?: number } } } } })
			.response?.data?.ocs?.meta
		if (meta) {
			if (meta.statuscode === 102) {
				// Username already taken.
				fields.value?.focusField('username')
			} else if (meta.statuscode === 107) {
				// Password policy rejected.
				fields.value?.focusField('password')
			}
		}
	}
}
</script>

<style lang="scss" scoped>
.dialog {
	:deep(.dialog__actions) {
		margin-block-start: calc(var(--default-grid-baseline, 4px) * 3);
	}
}

// Visually communicate the locked/busy form while the account is created.
.dialog__form[aria-busy='true'] {
	opacity: 0.5;
}
</style>
